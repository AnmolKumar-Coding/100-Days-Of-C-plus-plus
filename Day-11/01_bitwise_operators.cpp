//There are 4 bitwise operartors.
//1) AND -- &
//2) OR  -- |
//3) NOT -- ~
//4) XOR -- ^

#include <iostream>
using namespace std;

int main(){

    int a = 4;
    int b = 6;

    cout << "(a&b) = " << (a&b) << "\n" ;
    cout << "(a|b) = " << (a|b) << "\n" ;
    cout << "(~a) = " << (~a) << "\n" ;
    cout << "(a^b) = " << (a^b) << "\n" ;
}
